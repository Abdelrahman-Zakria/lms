import '../../domain/entities/user.dart';

class UserModel extends User {
  const UserModel({
    required super.id,
    required super.name,
    super.email,
    required super.isVip,
    required super.references,
    required super.points,
    required super.attendanceWeeks,
    required super.phone,
    required super.phoneVerified,
    super.countryCode,
    required super.fakeNumber,
    required super.type,
    required super.gendar,
    required super.mobileId,
    required super.mobileType,
    super.img,
    super.socialId,
    required super.username,
    required super.lock,
    super.lockMessage,
    required super.systemId,
    required super.stageId,
    required super.classroomId,
    required super.termId,
    super.pathId,
    super.institutionId,
    required super.createdAt,
    required super.updatedAt,
    required super.token,
    required super.system,
    required super.stage,
    required super.classroom,
    required super.term,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: json['id'] ?? 0,
      name: json['name'] ?? '',
      email: json['email'],
      isVip: json['is_vip'] ?? false,
      references: json['references'] ?? 0,
      points: json['points'] ?? 0,
      attendanceWeeks: json['attendance_weeks'] ?? 0,
      phone: json['phone'] ?? '',
      phoneVerified: json['phone_verified'] ?? 0,
      countryCode: json['country_code'],
      fakeNumber: json['fake_number'] ?? 0,
      type: json['type'] ?? '',
      gendar: json['gendar'] ?? '',
      mobileId: json['mobile_id'] ?? '',
      mobileType: json['mobile_type'] ?? '',
      img: json['img'],
      socialId: json['social_id'],
      username: json['username'] ?? '',
      lock: json['lock'] ?? '',
      lockMessage: json['lock_message'],
      systemId: json['system_id'] ?? 0,
      stageId: json['stage_id'] ?? 0,
      classroomId: json['classroom_id'] ?? 0,
      termId: json['term_id'] ?? 0,
      pathId: json['path_id'],
      institutionId: json['institution_id'],
      createdAt: json['created_at'] ?? '',
      updatedAt: json['updated_at'] ?? '',
      token: json['token'] ?? '',
      system: SystemInfo(
        id: json['system']['id'] ?? 0,
        name: json['system']['name'] ?? '',
      ),
      stage: StageInfo(
        id: json['stage']['id'] ?? 0,
        name: json['stage']['name'] ?? '',
      ),
      classroom: ClassroomInfo(
        id: json['classroom']['id'] ?? 0,
        name: json['classroom']['name'] ?? '',
      ),
      term: TermInfo(
        id: json['term']['id'] ?? 0,
        name: json['term']['name'] ?? '',
      ),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'email': email,
      'is_vip': isVip,
      'references': references,
      'points': points,
      'attendance_weeks': attendanceWeeks,
      'phone': phone,
      'phone_verified': phoneVerified,
      'country_code': countryCode,
      'fake_number': fakeNumber,
      'type': type,
      'gendar': gendar,
      'mobile_id': mobileId,
      'mobile_type': mobileType,
      'img': img,
      'social_id': socialId,
      'username': username,
      'lock': lock,
      'lock_message': lockMessage,
      'system_id': systemId,
      'stage_id': stageId,
      'classroom_id': classroomId,
      'term_id': termId,
      'path_id': pathId,
      'institution_id': institutionId,
      'created_at': createdAt,
      'updated_at': updatedAt,
      'token': token,
      'system': {
        'id': system.id,
        'name': system.name,
      },
      'stage': {
        'id': stage.id,
        'name': stage.name,
      },
      'classroom': {
        'id': classroom.id,
        'name': classroom.name,
      },
      'term': {
        'id': term.id,
        'name': term.name,
      },
    };
  }

  factory UserModel.fromEntity(User user) {
    return UserModel(
      id: user.id,
      name: user.name,
      email: user.email,
      isVip: user.isVip,
      references: user.references,
      points: user.points,
      attendanceWeeks: user.attendanceWeeks,
      phone: user.phone,
      phoneVerified: user.phoneVerified,
      countryCode: user.countryCode,
      fakeNumber: user.fakeNumber,
      type: user.type,
      gendar: user.gendar,
      mobileId: user.mobileId,
      mobileType: user.mobileType,
      img: user.img,
      socialId: user.socialId,
      username: user.username,
      lock: user.lock,
      lockMessage: user.lockMessage,
      systemId: user.systemId,
      stageId: user.stageId,
      classroomId: user.classroomId,
      termId: user.termId,
      pathId: user.pathId,
      institutionId: user.institutionId,
      createdAt: user.createdAt,
      updatedAt: user.updatedAt,
      token: user.token,
      system: user.system,
      stage: user.stage,
      classroom: user.classroom,
      term: user.term,
    );
  }
}
